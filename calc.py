# Function to add two numbers 
def add(num1, num2): 
	return num1 + num2
def sub(num1, num2): 
	return num1 + num2

number_1 = int(input("Enter first number: ")) 
number_2 = int(input("Enter second number: ")) 
print(number_1, "+", number_2, "=", add(number_1, number_2))
print(number_1, "-", number_2, "=", sub(number_1, number_2))

